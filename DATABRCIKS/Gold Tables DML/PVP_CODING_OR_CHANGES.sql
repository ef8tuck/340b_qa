UPDATE psas_di_qat.340b_slvr.T_LUTL_PVP_CODING AS LUPVCOD SET  LUPVCOD.Coding = concat( LUPVCOD.Chain , LUPVCOD.SUB_GROUP),  LUPVCOD.PVP_CODING = 'Y'
WHERE ((( LUPVCOD.Coding) Is Null Or ( LUPVCOD.Coding)=''));

-- COMMAND ----------

MERGE INTO psas_di_qat.340b_gold.T_PHS_AUDIT_2 AS PHAUD
USING psas_di_qat.340b_slvr.T_LUTL_PVP_CODING AS LUPVCOD
ON PHAUD.PVP_CODE = LUPVCOD.Coding
WHEN MATCHED AND PHAUD.PVP_CODING <> 'Y' AND LUPVCOD.PVP_CODING = 'Y' AND PHAUD.NATIONAL_GRP_CD <> '0100'
THEN UPDATE SET PHAUD.PVP_CODING = 'Y';

-- COMMAND ----------


--DROP VIEW IF EXISTS psas_di_qat.340b_gold.V_PVP_CODING_AUDIT;
TRUNCATE table psas_di_qat.340b_gold.T_PVP_CODING_AUDIT;
INSERT INTO psas_di_qat.340b_gold.T_PVP_CODING_AUDIT 
SELECT PHAUD.CUST_ACCT_ID, PHAUD.CUST_ACCT_NAME, PHAUD.ZX_BLOCK, PHAUD.PHS_340B_ID, IFF(PHAUD.PVP_PARTICIPATION_FLAG='Y','Y','N') AS PVP_Flag, PHAUD.PVP_CODE, PHAUD.PVP_CODING AS PVP_Coding, PHAUD.CUST_CHN_ID, PHAUD.NATIONAL_SUBGRP_CD, PHAUD.CHAIN_NAM, PHAUD.NATIONAL_SUBGRP_NAME, PHAUD.NATIONAL_GRP_NAME, PHAUD.PVP_EXPIRATION_DATE, PHAUD.HRSA_START_DATE AS HRSA_Start, PHAUD.COMMENTS, PHAUD.PVP_ELIGIBILITY_DATE AS PVP_Start, PHAUD.HRSA_TERM_DATE
FROM psas_di_qat.340b_gold.T_PHS_AUDIT_2 AS PHAUD 
WHERE (((PHAUD.PHS_340B_ID) IS NOT NULL) AND (IFF(PVP_PARTICIPATION_FLAG='Y','Y','N') != PVP_CODING) AND (PHAUD.CUST_CHN_ID != '989') AND (PHAUD.ACCT_CLASSIFICATION in ('004','005')) AND (UPPER(PHAUD.STATUS)='ACTIVE'))
ORDER BY PHAUD.PVP_ELIGIBILITY_DATE;

CREATE OR REPLACE view psas_di_qat.340b_gold.V_PVP_CODING_AUDIT AS SELECT * FROM psas_di_qat.340b_gold.T_PVP_CODING_AUDIT;


