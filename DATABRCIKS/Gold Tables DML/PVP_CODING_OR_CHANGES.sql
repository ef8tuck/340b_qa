FROM psas_di_qat.340b_gold.T_PHS_AUDIT_2 AS PHAUD LEFT JOIN psas_di_qat.`340b_brnz`.T_OPACE_DAILY_PUBLIC AS OPACE_DAILY_PUBLIC ON PHAUD.PHS_340B_ID = OPACE_DAILY_PUBLIC.340B_ID
